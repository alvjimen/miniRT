#include "str.h"

int	ft_is_str_uniq(char *str)
{
	size_t	c_1;
	size_t	c_2;

	c_2 = 0;
	while (str[c_2])
	{
		c_1 = c_2 + 1;
		while (str[c_1])
		{
			if (str[c_2] == str[c_1] && c_2 != c_1)
				return (0);
			c_1++;
		}
		c_2++;
	}
	return (1);
}
